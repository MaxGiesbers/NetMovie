@Composable
fun MovieScreen(navHostController: NavHostController,  movieViewModel: MovieViewModel = hiltViewModel()) {

    val trailer = movieViewModel.trailerId.collectAsState()
    Column(
        modifier = Modifier
            .fillMaxWidth(1f)
            .fillMaxHeight(),
        verticalArrangement = Arrangement.Top,
        horizontalAlignment = Alignment.CenterHorizontally) {
        Spacer(modifier = Modifier.height(10.dp))

        trailer.value?.let {
            YoutubeScreen(it, Modifier
                .fillMaxWidth(1f)
                .fillMaxHeight())
        }
        Text(text = movieViewModel.movieId)
    }
}

@Composable
fun YoutubeScreen(
    videoId: String,
    modifier: Modifier
) {
    val ctx = LocalContext.current
    AndroidView(factory = {
        var view = YouTubePlayerView(it)
        val fragment = view.addYouTubePlayerListener(
            object : AbstractYouTubePlayerListener() {
                override fun onReady(youTubePlayer: YouTubePlayer) {
                    super.onReady(youTubePlayer)
                    youTubePlayer.loadVideo(videoId, 0f)
                }
            }
        )
        view
    })
}

